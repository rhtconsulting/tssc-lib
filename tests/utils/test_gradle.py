class TestGradleUtils_run_gradle(BaseTestCase):
    @patch('sh.gradle', create=True)
    @patch('ploigos_step_runner.utils.gradle.create_sh_redirect_to_multiple_streams_fn_callback')
    @patch("builtins.open", new_callable=mock_open)
    def test_success_defaults(self, mock_open, redirect_mock, mvn_mock):
        with TempDirectory() as temp_dir:
            gradle_output_file_path = os.path.join(temp_dir.path, 'gradle_output.txt')
            build_file = '/fake/build.gradle'
            tasks = 'fake'

            run_gradle(
                gradle_output_file_path=gradle_output_file_path,
                build_file=build_file,
                tasks=tasks,
            )

            mock_open.assert_called_with(gradle_output_file_path, 'w', encoding='utf-8')
            redirect_mock.assert_has_calls([
                call([
                    sys.stdout,
                    mock_open.return_value,
                    Any(IOBase)
                ]),
                call([
                    sys.stderr,
                    mock_open.return_value
                ])
            ])

            mvn_mock.assert_called_once_with(
                '-b', '/fake/build.gradle',
                '--console=plain',
                 ['fake'],
                _out=Any(StringIO),
                _err=Any(StringIO)
            )